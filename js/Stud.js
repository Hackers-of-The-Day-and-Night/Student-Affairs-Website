class Stud {
  id;
  name;
  email;
  phone;
  dob;
  gender;
  gpa;
  level;
  dept;
  status;
  constructor(id, name, email, phone, dob, gender, gpa, level, dept, status) {
    this.id = id;
    this.name = name;
    this.email = email;
    this.phone = phone;
    this.dob = dob;
    this.gender = gender;
    this.gpa = gpa;
    this.level = level;
    this.dept = dept;
    this.status = status;
  }
}
